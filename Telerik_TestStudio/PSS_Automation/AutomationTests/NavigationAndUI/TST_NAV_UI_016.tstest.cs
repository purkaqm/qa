    public class TST_NAV_UI_016 : BaseWebAiiTest
    {
        #region [ Dynamic Pages Reference ]

        private Pages _pages;

        /// <summary>
        /// Gets the Pages object that has references
        /// to all the elements, frames or regions
        /// in this project.
        /// </summary>
        public Pages Pages
        {
            get
            {
                if (_pages == null)
                {
                    _pages = new Pages(Manager.Current);
                }
                return _pages;
            }
        }

        #endregion
        
        // Add your test methods here...
    
        [CodedStep(@"New Coded Step")]
        public void TST_NAV_UI_016_CodedStep()
        {
            Pages.PS_HomePage.ProfilePicSettingSpan.Wait.ForVisible();
            Pages.PS_HomePage.ProfilePicSettingSpan.MouseHover();
            Assert.IsTrue(Pages.PS_HomePage.ProfilePicSettingSpan.BaseElement.GetAttribute("title").Value.Equals("You are logged in as Administrator"),"user should be admin");
            string iconColor = Data["Expected_Color"].ToString();
            Assert.IsTrue(Pages.PS_HomePage.ProfilePicSettingSpan.GetComputedStyleValue("color").Equals(iconColor),"color should be matched");
        }
    }

namespace PSS_Automation
{

    public class TST_MAT_025_C992433 : BaseWebAiiTest
    {
        #region [ Dynamic Pages Reference ]

        private Pages _pages;

        /// <summary>
        /// Gets the Pages object that has references
        /// to all the elements, frames or regions
        /// in this project.
        /// </summary>
        public Pages Pages
        {
            get
            {
                if (_pages == null)
                {
                    _pages = new Pages(Manager.Current);
                }
                return _pages;
            }
        }

        #endregion
        
        // Add your test methods here...
    
        [CodedStep(@"Wait for user to be navigated to newly created work summary page")]
        public void TST_MAT_025_C992433_WaitSummaryPage()
        {
            Pages.PS_ProjectSummaryPage.ProjectSummaryContentDiv.Wait.ForExists();
            Pages.PS_ProjectSummaryPage.ProjectSummaryDescendantsDiv.Wait.ForExists();
            Assert.IsTrue(Pages.PS_HomePage.PageTitleDiv.InnerText.Contains("Summary"),"page tiltle should contain 'Summary' text");
                                                                                                   
        }
    
        [CodedStep(@"Verify summary page is displayed correctly")]
        public void TST_MAT_024_C992433_VerifyProjectSummary()
        {
            Assert.IsTrue(Pages.PS_HomePage.PageTitleDiv.InnerText.Contains(GetExtractedValue("CreatedProjectName").ToString()),"Correct project name should be displayed on page title");
            Assert.IsTrue(Pages.PS_ProjectSummaryPage.DetailsEditLink.IsVisible(),"Edit link should be visible");
            Assert.IsTrue(Pages.PS_ProjectSummaryPage.ProjectSummaryContentDiv.IsVisible());
            Assert.IsTrue(Pages.PS_ProjectSummaryPage.ProjectSummaryDescendantsDiv.IsVisible(),"Descendent should be visible");
        }
    }

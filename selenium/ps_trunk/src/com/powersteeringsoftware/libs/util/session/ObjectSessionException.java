package com.powersteeringsoftware.libs.util.session;

public class ObjectSessionException extends Exception {

    private static final long serialVersionUID = 1L;

    public ObjectSessionException() {
        super();
    }

    public ObjectSessionException(String message, Throwable cause) {
        super(message, cause);
    }

    public ObjectSessionException(String message) {
        super(message);
    }

    public ObjectSessionException(Throwable cause) {
        super(cause);
    }

}
